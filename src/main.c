#include "minishell.h"

int	g_sig;

void	init_data(t_data *data)
{
	data->input = NULL;
	data->buffercwd = NULL;
	data->sigint = 0;
	data->init_env = 0;
	data->builtins = NULL;
	data->home_env = NULL;
	data->buffersize = 0;
	data->pipes = 0;
	data->lex = NULL;
	data->cmd = NULL;
	data->copy_env = NULL;
	data->std_in = do_dup(STDIN_FILENO);
	data->std_out = do_dup(STDOUT_FILENO);
	data->exit = 0;
	data->pipe_tab = NULL;
	data->pid_tab = NULL;
}

int	main(int argc, char **argv, char **env)
{
	t_data	*data;
	t_data	temp;

	if (argc != 1)
	{
		printf("Error\n");
		exit(0);
	}
	data = &temp;
	init_data(data);
	(void)argv;
	init_env(data, env);
	g_sig = 0;
	ft_signal();
	while (1 && data->exit == 0)
		ft_prompt(data);
	printf("exit\n");
	free_env_list(data);
	return (g_sig);
}

